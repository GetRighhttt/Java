package basics;

public class Variables {

    /*
    Let's begin by understanding the basics of java variables.
     */
    public static void main(String[] args) {
        int x = 30;
        System.out.println("Hooray, my first Java app at age " + x);

        for(int i = 1; i <= x; ++i) {
            System.out.println("This is a for loop of i to x which is 30: " + i);
        }



    }
}
